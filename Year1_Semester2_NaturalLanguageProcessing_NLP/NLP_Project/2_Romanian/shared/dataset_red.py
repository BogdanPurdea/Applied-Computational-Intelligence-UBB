"""
dataset_red.py
==============
Load the pre-built REDv2 CSV splits for use in transformer fine-tuning.

The three CSVs (train.csv, valid.csv, test.csv) are generated by
``REDv2/dataset_red.py`` and already live in ``data/red/``.
This module is the single source of truth for dataset access within
the Practical_Project training and evaluation scripts.

CSV schema
----------
text_id, text,
agreed_label_{sadness,surprise,fear,anger,neutral,trust,joy}  (int 0/1),
pct_label_{sadness,surprise,fear,anger,neutral,trust,joy}     (float),
num_emotions, is_multilabel, has_short_text

Usage
-----
    from dataset_red import load_splits, load_preprocessed, save_preprocessed

    df_train, df_val, df_test = load_splits("classification")
"""

from __future__ import annotations

import sys
from pathlib import Path

import pandas as pd

# Add project root to path so we can do imports
project_root = Path(__file__).resolve().parents[2]
sys.path.insert(0, str(project_root))
sys.path.insert(0, str(project_root / "shared" / "migrated"))
from dataset_utils import save_pickle_cache, load_pickle_cache

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

EMOTION_LABELS: list[str] = [
    "sadness",   # index 0  (Tristețe)
    "surprise",  # index 1  (Surpriză)
    "fear",      # index 2  (Frică)
    "anger",     # index 3  (Furie)
    "neutral",   # index 4  (Neutru)
    "trust",     # index 5  (Încredere)
    "joy",       # index 6  (Bucurie)
]

AGREED_LABEL_COLS: list[str] = [f"agreed_label_{e}" for e in EMOTION_LABELS]
PCT_LABEL_COLS:    list[str] = [f"pct_label_{e}"    for e in EMOTION_LABELS]

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------

DATA_DIR     = project_root / "Datasets" / "migrated" / "red"

_SPLIT_FILES = {
    "train": DATA_DIR / "train.csv",
    "val":   DATA_DIR / "valid.csv",   # file on disk is named "valid.csv"
    "test":  DATA_DIR / "test.csv",
}


# ---------------------------------------------------------------------------
# Split loader
# ---------------------------------------------------------------------------

def load_splits(
    task: str = "classification",
) -> tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    """Load the three REDv2 CSV splits and return DataFrames.

    The splits are pre-built (not computed here) and already guaranteed to
    have no text_id overlap between train / valid / test.

    Args:
        task: Which label columns to include in the returned DataFrames.
              ``"classification"`` → ``agreed_label_*`` columns (int 0/1).
              ``"regression"``     → ``pct_label_*`` columns (float).
              ``"both"``           → all label columns.

    Returns:
        ``(df_train, df_val, df_test)`` DataFrames.
    """
    if task == "classification":
        label_cols = AGREED_LABEL_COLS
    elif task == "regression":
        label_cols = PCT_LABEL_COLS
    elif task == "both":
        label_cols = AGREED_LABEL_COLS + PCT_LABEL_COLS
    else:
        raise ValueError(
            f"task must be 'classification', 'regression', or 'both'; got {task!r}"
        )

    meta_cols = ["text_id", "text", "num_emotions", "is_multilabel", "has_short_text"]
    keep_cols = meta_cols + label_cols

    dfs = []
    for split in ("train", "val", "test"):
        csv_path = _SPLIT_FILES[split]
        if not csv_path.exists():
            raise FileNotFoundError(
                f"REDv2 CSV not found at '{csv_path}'.\n"
                f"Run REDv2/dataset_red.py first to generate the CSVs."
            )
        df = pd.read_csv(csv_path)
        # Only keep columns that exist (gracefully handles both task modes)
        df = df[[c for c in keep_cols if c in df.columns]]
        dfs.append(df.reset_index(drop=True))

    df_train, df_val, df_test = dfs
    print(
        f"[dataset_red] Splits — "
        f"train: {len(df_train):,}  val: {len(df_val):,}  test: {len(df_test):,}"
    )
    return df_train, df_val, df_test


# ---------------------------------------------------------------------------
# Preprocessed text cache helpers
# ---------------------------------------------------------------------------

def save_preprocessed(texts: list[str], split: str) -> Path:
    """Pickle a list of preprocessed texts to ``data/red/preprocessed_red_{split}.pkl``.

    Args:
        texts: List of cleaned text strings.
        split: One of ``"train"``, ``"val"``, ``"test"``.

    Returns:
        Path to the saved pickle file.
    """
    path = DATA_DIR / f"preprocessed_red_{split}.pkl"
    save_pickle_cache(texts, path)
    print(f"[dataset_red] Saved preprocessed {split} texts → {path}")
    return path


def load_preprocessed(split: str) -> list[str] | None:
    """Load cached preprocessed texts, or return None if cache is missing.

    Args:
        split: One of ``"train"``, ``"val"``, ``"test"``.

    Returns:
        List of cleaned text strings, or ``None`` if not yet cached.
    """
    path = DATA_DIR / f"preprocessed_red_{split}.pkl"
    texts = load_pickle_cache(path)
    if texts is not None:
        print(
            f"[dataset_red] Loaded preprocessed {split} texts from cache "
            f"({len(texts):,} items)"
        )
    return texts


# ---------------------------------------------------------------------------
# CLI entry point — run as: python dataset_red.py
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    print("=" * 60)
    print("REDv2 Dataset — Classification Split Preview")
    print("=" * 60)

    df_train, df_val, df_test = load_splits("classification")

    print("\nLabel frequency per split:")
    for name, df in [("train", df_train), ("val", df_val), ("test", df_test)]:
        freq = {e: int(df[f"agreed_label_{e}"].sum()) for e in EMOTION_LABELS}
        print(f"  {name:5s}: {freq}")

    print("\nFirst row (train):")
    print(df_train.iloc[0].to_string())
